public class OperatorTest008{
    public static void main(String[] args){
        boolean  sex=true; 
        char     kkk=sex?'男':'女';            //三目运算符
		                                   
        System.out.println(kkk);               //sex为true，因此结果为男，变量名当然可以多个字母
		System.out.println(sex?'男':'女');     //此处语法正确，可以编译，结果为男



}




}