public class ForTest007{
    public static void main(String[] args){
	
	    for(int c=1;c<=5;c++){
		
		    for(int d=1;d<=1000;d*=10){
			
			    System.out.print(c*d+" ");   
			
			}
		   System.out.println();          
		}
	
	}

}
