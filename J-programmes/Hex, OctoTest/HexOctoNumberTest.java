

public class HexOctoNumberTest {

    public static void main(String[] args) {

        //16进制
        int hex = 0x3050;
        int hex1 = 0x10;
        System.out.println(hex);  //这里默认打印成十进制
        System.out.println(hex1);

        //8进制
        int octo = 015;
        System.out.println(octo);

    }
}
