public class ForTest005{
    public static void main(String[] args){
	    for(int x=1;x<=9;x++){
		    
			if(x==1){
			   for(int y=1;y<=1;y++){
			       System.out.print(x+"x"+y+"="+(x*y)+'\n');
			   }
			   
			}

			if(x==2){
			   for(int y=1;y<=2;y++){
			      System.out.print(x+"*"+y+"="+(x*y)+" ");
			   }
			
			}
			
		
		}
	
	
	}

}


